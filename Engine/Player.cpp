#include "Player.h"
#include "Graphics.h"

void Player::Update() {

	x = x + vx;
	y = y + vy;

}

void Player::ClampScreen() {
	if (x < 0) {
		x = 0;
		vx = -vx;

	}
	else if (x + width >= Graphics::ScreenWidth) {
		x = Graphics::ScreenWidth - width;
		vx = -vx;
	}

	if (y < 0) {
		y = 0;
		vy = -vy;

	}
	else if (y + height >= Graphics::ScreenHeight) {
		y = Graphics::ScreenHeight - height;
		vy = -vy;
	}
}

int Player::animationStatus()
{
	
	if (vx == -3) {
		return 3; // handles negative x direction animation
	}
	else if (vx == 3) {
		return 1; // handles positive x direction animation
	}

	if (vy == -3 || vx == 0 || vy == 3) {
		return 0; // handles positive and negative y direction animation
	} 
}


Player::Player(int in_x, int in_y, int in_vx, int in_vy)
{
	x = in_x;
	y = in_y;
	vx = in_vx;
	vy = in_vy;
}

void Player::HandleInput(MainWindow &wnd) {
	if (movable) {
		if (wnd.kbd.KeyIsPressed('W') || wnd.kbd.KeyIsPressed(VK_UP)) {

			vy = -3;
			vx = 0;
		}

		if (wnd.kbd.KeyIsPressed('S') || wnd.kbd.KeyIsPressed(VK_DOWN)) {

			vx = 0;
			vy = 3;
		}

		if (wnd.kbd.KeyIsPressed('D') || wnd.kbd.KeyIsPressed(VK_RIGHT)) {

			vy = 0;
			vx = 3;

		}
		if (wnd.kbd.KeyIsPressed('A') || wnd.kbd.KeyIsPressed(VK_LEFT)) {

			vy = 0;
			vx = -3;
		}
	}
}

